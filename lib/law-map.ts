/**
 * 처리 유형에 따라 필요한 서류 세트를 결정한다.
 *
 * 서류 세트 판단표:
 * - 현장종결: A만
 * - 타부서인계(일반): A + C + B
 * - 타부서인계(절도): A + C (절도는 B 불요)
 * - 현행범체포: A + D + B (D가 C를 대체)
 * - 가정폭력 긴급임시조치: +E
 * - 스토킹 긴급응급조치: +F
 * - 압수물 있음: +압수경위서
 */
export function getRequiredDocuments(
  incidentType: IncidentType,
  dispositionType: string,
  options?: { hasSeizure?: boolean; isDomesticViolence?: boolean; isStalking?: boolean }
): DocumentCode[] {
  // A(112종결내용)는 모든 경우 필수
  const docs: DocumentCode[] = ["A"];

  if (dispositionType === "현장종결") {
    return docs;
  }

  if (dispositionType === "현행범체포") {
    // 현행범체포 시 D(체포서)가 C(발생보고서)를 대체, B는 포함
    docs.push("D", "B");
  } else if (dispositionType === "타부서인계_절도" || incidentType === "절도") {
    // 절도 인계: C만 (B 불요)
    docs.push("C");
  } else {
    // 타부서인계_일반: C + B
    docs.push("C", "B");
  }

  // 가정폭력 긴급임시조치 실시 시 E 추가
  if (options?.isDomesticViolence || incidentType === "가정폭력") {
    if (!docs.includes("E")) docs.push("E");
  }

  // 스토킹 긴급응급조치 실시 시 F 추가
  if (options?.isStalking || incidentType === "스토킹") {
    if (!docs.includes("F")) docs.push("F");
  }

  // 압수물 있음 시 압수경위서 추가
  if (options?.hasSeizure) {
    docs.push("seizure");
  }

  return docs;
}
