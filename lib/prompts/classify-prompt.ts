import type { IncidentType } from "../types";

const SUPPORTED_TYPES: IncidentType[] = [
  "가정폭력",
  "폭행상해",
  "절도",
  "교통사고",
  "자살자해",
  "실종",
  "스토킹",
  "주거침입",
  "사기",
  "손괴",
  "모욕명예훼손",
  "기타",
];

/**
 * 분류 + 매뉴얼 생성 사용자 프롬프트를 조합한다.
 */
export function buildClassifyUserPrompt(reportContent: string): string {
  return `# 신고 내용
${reportContent}

# 요청
위 신고 내용을 분석하여 아래 형식으로 응답하세요.

## 1단계: 분류 결과 (JSON)
반드시 아래 JSON 블록으로 시작하세요:
\`\`\`json
{
  "incidentType": "${SUPPORTED_TYPES.join(" | ")}",
  "subType": "세부 유형 (있으면)",
  "summary": "신고 내용 한 줄 요약"
}
\`\`\`

## 2단계: 현장 처리 매뉴얼
분류 결과 이후, 아래 섹션을 순서대로 작성하세요.

### ⚠️ 리스크 플래그
- 현장에서 즉시 주의해야 할 사항을 bullet으로 나열

### 📋 현장 처리 절차
- 단계별로 번호를 매겨 작성
- 판단 분기점이 있으면 하위 항목으로 표시

### ⚖️ 판단 기준
- 핵심 판단 기준 (응급조치/체포/현장종결 등 분기 조건)

### 📎 필요 증거 체크리스트
- 현장에서 확보해야 할 증거 목록

### 📄 예상 필요 서류
- 사안 유형에 따라 필요한 서류 목록과 코드

### 📖 관련 법령
- 제공된 법령 원문 중 핵심 조문을 인용하여 설명
- 법령 원문이 제공되지 않은 경우 [법령 조회 실패 — 직접 확인 필요]로 표시`;
}
